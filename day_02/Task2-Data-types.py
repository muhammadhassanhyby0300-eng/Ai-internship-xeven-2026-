"""
Explainations of Data type with their exampes
"""
# integer 
age = 19
print("Age:",age, "| Type:", type(age))

# Float
pi=3.14
print("Pi:",pi, "|Type:",type(pi))

# Boolean
is_active = False
print("Active:",is_active,"|Type:",type(is_active))

# String
place = "Lahore"
print("Place:",place,"|Type:",type(place))

# String to number
str_num = 40.34
float_num =float(str_num)
print("Converted string:",float_num,"|Type:",type(float_num))

# Float to integer
float_to_int = int(pi)
print("Pi as int",float_to_int,"|Type:",type(float_to_int))